export const locale = "zh-CN";

export const getAppTranslation = (key: string, fallback?: string) => {
  const value = key.split(".").reduce((acc: unknown, segment) => {
    if (acc && typeof acc === "object" && segment in (acc as Record<string, unknown>)) {
      return (acc as Record<string, unknown>)[segment];
    }

    return undefined;
  }, messages as unknown);

  return value ?? fallback ?? key;
};

export const getAppMessage = <T = string>(key: string, fallback?: T): T => {
  return getAppTranslation(key, fallback) as T;
};
